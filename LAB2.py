#SUM OF THE SQUAREROOT OF TWO NUMBERS >>>
import math
sum=0
num1= int(input("Enter the first number: "))
num2= int(input("Enter the second number: "))
num3= int(input("Enter the third number: "))

sq1= math.sqrt(num1) 
sq2= math.sqrt(num2)
sq3= math.sqrt(num3)

sum=sq1+sq2+sq3

print("Sum of the square root of 3 numbers are: "+sum)

